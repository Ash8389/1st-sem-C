#include<stdio.h>

int main()
{
   int a [] = {32,34,35};
   printf("The value of a[0] is %d\n", a[0]);
   printf("The value of a[1] is %d\n", a[1]);
   printf("The value of a[2] is %d\n", a[2]);

   float b [] = {3.2,3.4,3.5};
   printf("The value of a[0] is %f\n", b[0]);
   printf("The value of a[1] is %f\n", b[1]);
   printf("The value of a[2] is %f\n", b[2]);

   char c [] = {'a','b','c'};
   printf("The value of a[0] is %c\n", c[0]);
   printf("The value of a[1] is %c\n", c[1]);
   printf("The value of a[2] is %c\n", c[2]);


    return 0;
}